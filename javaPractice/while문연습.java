package 두번째날;

public class while문연습 {

	public static void main(String[] args) {
		// N번 반복

		int N = 10;
		int i = 0;
		while (i < N) {
			System.out.println(i);
			i++;
		}

		System.out.println();

		// a-b 반복
		int a = 3;
		int b = 5;
		while (a <= b) {
			System.out.println(a);
			a++;
		}

	}

}
